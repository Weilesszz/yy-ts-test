<!-- eslint-disable vue/no-mutating-props -->
<template>
  <div class="yy-form">
    <div class="header">
      <slot name="header"></slot>
    </div>
    <el-form :label-width="labelWidth">
      <el-row>
        <template v-for="item in formItem" :key="item.label">
        <el-col v-bind="colLayout">
            <el-form-item
              v-if="!item.isHidden"
              :label="item.label"
              :rules="item.rules"
              :style="myItemStyle"
            >
              <template v-if="item.type==='input'||item.type==='password'">
                <el-input :show-password="item.type==='password'" :placeholder="item.placeholder" :model-value="modelValue[`${item.field}`]" @update:modelValue="handleValueChange($event, item.field)"/>
              </template>
              <template v-else-if="item.type==='select'">
                <el-select style="width: 100%" :placeholder="item.placeholder" :model-value="modelValue[`${item.field}`]" @update:modelValue="handleValueChange($event, item.field)">
                  <el-option v-for="option in item.options" :key="option.value" :value="option.value">
                    {{option.title}}
                  </el-option>
                </el-select>
              </template>
              <template v-else-if="item.type==='datepicker'">
                <el-date-picker style="width:100%" v-bind="item.otherOptions" :model-value="modelValue[`${item.field}`]" @update:modelValue="handleValueChange($event, item.field)"></el-date-picker>
              </template>
            </el-form-item>
        </el-col>
        </template>
      </el-row>
    </el-form>
    <div class="footer">
      <slot name="footer"></slot>
    </div>
  </div>
</template>

<script lang="ts">

import { defineComponent, PropType } from 'vue';
import { IFormItem } from '../types';

export default defineComponent({
  props:{
    modelValue:{
      type:Object,
      default:()=>({})
    },
    // formData:{
    //   type:Object,
    //   requited:true
    // },
    formItem:{
      type:Array as PropType<IFormItem[]>,
      default:()=>[]
    },
    labelWidth:{
      type:String,
      default:'100px'
    },
    myItemStyle:{
      type:Object,
      default:()=>({padding:'10px 40px'})
    },
    colLayout:{
      type:Object,
      default:()=>({
        xl:6,
        lg:8,
        md:12,
        sm:24,
        xs:24
      })
    }
  },
  emits:['update:modelValue'],
  setup(props,{emit}) {
    // const formData=ref({...props.modelValue})
    // watch(formData,(newValue)=>{
    //   emit('update:modelValue',newValue)
    // },
    // {
    //   deep:true
    // })
    const handleValueChange=(value:any,field:string)=>{
      emit('update:modelValue',{...props.modelValue,[field]:value})
    }
    return {
      // formData
      handleValueChange
    }
  }
})
</script>

